// Operadores de comparação
console.log(10 == 10); // == ele retornará true ou false.
console.log(10 != "10"); // != Diferente de.
console.log(10 === "10"); // Estritamente igual.
console.log(10 !== "10"); // Estritamente desigual.
console.log(10 > 5); // Maior que.
console.log(10 >= 10); // Maior ou igual.
console.log(10 < 11); // Menor que.
console.log(10 <= 10); // Menor ou igual.